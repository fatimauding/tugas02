					<h2 align="center">Keterangan</h2>
					<hr>
					<p> &nbsp; &nbsp; &nbsp;  Pemandangan alam merupakan keindahan alam yang dapat dipersepsikan melalui komponen mata dalam panca indra manusia. Sketsa pemandangan alam dalam seni lukis biasanya terdiri dari komponen-komponen seperti pepohonan, semak-semak, daratan, bukit, lembah, air ataupun sejumlah benda buatan manusia contohnya rumah, jembatan, mobil dll. Pemandangan alam merupakan suatu hal yang perlu dilindungi atau dijaga karena bertujuan untuk melindungi keindahan alam. Indonesia merupakan salah satu negara yang cukup banyak memiliki pemandangan alam yang menarik dan indah misalnya negari Sianok (ngarai Sianok) di Sumatra Barat yang menjadi salah satu potensi wisata dengan fenomena alamnya yang Indah. Indonesia banyak memiliki potensi keindahan alam yang memiliki daya tarik yang sangat beraneka ragam sehingga Indonesia merupakan salah satu negara yang paling diminati untuk tujuan pariwisata. Selain pemandangan alam yang sangat potensial Indonesia juga merupakan negara yang kaya akan budaya sehingga dengan adanya variasi dan daya tarik tersendiri yang ditawarkan oleh Indonesia membuat Indonesia menjadi wilayah yang cukup menarik untuk dikunjungi. Dalam kegiatan seni lukis pemandangan alam merupakan salah satu objek yang paling banyak diminati.</p>

					<h3>Pemandangan Alam Danau</h3>

					<p> &nbsp; &nbsp; &nbsp;  Danau merupakan cekungan besar di permukanan bumi yang tergenang oleh air tawar maupun air asin dan dikelilingi oleh daratan. Secara umum danau ditemui di daerah pegunungan dan digunakan untuk sarana rekreasi dan olahraga karena paronamanya yang cukup indah.</p>
					<p> &nbsp; &nbsp; &nbsp; Selain sebagai keindahan alam danau juga memiliki manfaat yang penting bagi kehidupan mahluk hidup khususnya di sekitar danau.</p>

					<h3>Pemandangan Air Terjun</h3>

					<p> &nbsp; &nbsp; &nbsp; Air terjun merupakan formasi geologi arus air yang mengalir melalui suatu formasi batuan yang telah mengalami erosi dan jatuh kebawah dari ketinggian. Terdapat juga air terjun yang merupakan buatan manusia. Biasanya air terjun terbentuk di daerah lingkungan pegunungan yang sering terjadi erosi.</p>

					<h3>Pemandangan Alam Sungai</h3>

					<p> &nbsp; &nbsp; &nbsp; Selain pemandangan alam berupa sungai Kapus, di Indonesia ada juga Sungai yang terletak di provinsi Sumatera Selatan, yang dikenal sebagai sungai terpanjang di Pulau Sumatera dengan panjang 750 km, nama sungai tersebut adalah Sungai Musi dengan Jembatan terkenalnya ialah Ampera.</p>

					<p> &nbsp; &nbsp; &nbsp; Sungai ialah aliran air yang besar dan memanjang yang mengalir secara terus-menerus dari hulu (sumber) menuju hilir (muara). Sungai juga dapat dijadikan sebagai destinasi wisata. Beragam kegiatan yang dapat dilakukan ketika berwisata di sungai misalnya arung jeram atau rafting, river tubing dan river boarding.</p>

					<h3>Pemandangan Alam Karst</h3>

					<p> &nbsp; &nbsp; &nbsp;  Karst merupakan bentuk permukaan bumi yang pada umumnya dicirikan dengan adanya depresi tertutup (closed depression), drainase permukaan, dan gua.</p>

					<p> &nbsp; &nbsp; &nbsp; Indonesia merupakan salah satu negara yang kawasan karstnya mempunyai nilai yang sangat strategis. Potensi bentang alam karst Indonesia 154.000 km2 setara dengan 0.08% dari luas daratan Indonesia, karst mempunyai potensi yang bukan saja unik dan khas tetapi juga sangat kaya dengan sumberdaya alam baik itu hayati maupun non hayati.</p>

